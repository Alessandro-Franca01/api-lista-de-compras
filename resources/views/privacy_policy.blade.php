    <h1>Política de Privacidade do Meta</h1>

    <p>Última atualização: 25/04/2025 </p>

    <p>Bem-vindo à Política de Privacidade do Meta. Esta política descreve como coletamos, usamos, divulgamos, retemos e protegemos suas informações quando você utiliza nossos Produtos Meta. Nossos Produtos incluem o Facebook, Instagram, Messenger, WhatsApp, Oculus/Meta Quest, entre outros serviços e produtos oferecidos pelo Meta Platforms, Inc. e suas empresas afiliadas.</p>

    <p>Ao usar nossos Produtos Meta, você concorda com a coleta e uso de suas informações de acordo com esta política de privacidade. Não usaremos ou compartilharemos suas informações com ninguém, exceto conforme descrito nesta Política de Privacidade.</p>

    <h2>Quais informações coletamos?</h2>

    <p>Coletamos informações para fornecer, aprimorar, proteger e promover nossos Produtos Meta. Os tipos de informações que coletamos dependem de como você usa nossos Produtos. Aqui estão as categorias gerais de informações que podemos coletar:</p>

    <ul>
        <li><strong>Informações que você nos fornece:</strong> Inclui informações fornecidas ao criar uma conta, criar ou compartilhar conteúdo, comunicar-se com outros, e informações de pagamento.</li>
        <li><strong>Informações coletadas automaticamente:</strong> Coletamos informações sobre como você usa nossos Produtos, as ações que realiza, os recursos que utiliza, o tempo, a frequência e a duração de suas atividades. Isso também pode incluir informações sobre os dispositivos que você usa para acessar nossos Produtos, como tipo de dispositivo, sistema operacional, configurações, identificadores únicos, e dados de conexão (como endereço IP).</li>
        <li><strong>Informações de terceiros:</strong> Podemos receber informações sobre você de terceiros, incluindo parceiros, anunciantes e outros usuários dos Produtos Meta.</li>
        <li><strong>Informações de cookies e tecnologias semelhantes:</strong> Utilizamos cookies e tecnologias semelhantes para coletar informações sobre seu uso de nossos Produtos e de sites e serviços de terceiros.</li>
    </ul>

    <h2>Como usamos suas informações?</h2>

    <p>Usamos as informações que coletamos para:</p>

    <ul>
        <li>Fornecer, operar, manter, aprimorar, entender e personalizar nossos Produtos Meta.</li>
        <li>Desenvolver novos produtos e recursos.</li>
        <li>Comunicar com você, incluindo sobre atualizações de produtos, segurança e suporte.</li>
        <li>Exibir anúncios relevantes para você.</li>
        <li>Pesquisa e inovação.</li>
        <li>Promover segurança e integridade.</li>
        <li>Conformidade legal e regulatória.</li>
    </ul>

    <h2>Como suas informações são compartilhadas?</h2>

    <p>Podemos compartilhar suas informações com:</p>

    <ul>
        <li><strong>Empresas do Meta:</strong> Compartilhamos informações entre as empresas do Meta para operar e fornecer nossos Produtos.</li>
        <li><strong>Parceiros e terceiros:</strong> Podemos compartilhar informações com parceiros que nos ajudam a fornecer e aprimorar nossos Produtos, como provedores de serviços, anunciantes e parceiros de medição.</li>
        <li><strong>Para fins legais e de segurança:</strong> Podemos divulgar informações em resposta a solicitações legais, para cumprir com leis aplicáveis, para prevenir fraudes, proteger nossos direitos e propriedades, e a segurança de usuários e outros.</li>
        <li><strong>Com seu consentimento:</strong> Podemos compartilhar suas informações com terceiros quando você nos der seu consentimento.</li>
    </ul>

    <h2>Por quanto tempo retemos suas informações?</h2>

    <p>Retemos informações pelo tempo necessário para os fins descritos nesta Política de Privacidade e de acordo com as leis aplicáveis.</p>

    <h2>Como protegemos suas informações?</h2>

    <p>Implementamos medidas de segurança técnicas e organizacionais para proteger suas informações contra acesso não autorizado, alteração, divulgação ou destruição.</p>

    <h2>Seus direitos e controles</h2>

    <p>Você pode ter certos direitos em relação às suas informações, dependendo das leis de proteção de dados aplicáveis. Estes podem incluir o direito de acessar, corrigir, excluir ou restringir o uso de suas informações. Oferecemos ferramentas nas configurações de nossos Produtos Meta para ajudá-lo a gerenciar sua privacidade.</p>

    <h2>Transferências Internacionais de Dados</h2>

    <p>Podemos transferir suas informações para fora do seu país de residência para os locais onde operamos. Ao usar nossos Produtos Meta, você concorda com a transferência de suas informações para fora do seu país.</p>

    <h2>Alterações nesta Política de Privacidade</h2>

    <p>Podemos atualizar nossa Política de Privacidade periodicamente. Notificaremos você sobre quaisquer alterações publicando a nova Política de Privacidade nesta página. Recomendamos que você revise esta Política de Privacidade periodicamente para quaisquer alterações.</p>

    <h2>Entre em contato conosco</h2>

    <p>Se você tiver dúvidas sobre esta Política de Privacidade, entre em contato conosco através dos canais apropriados fornecidos pelo Meta.</p>

